#include "libft.h"

int	ft_atoi(const char *str)
{
	int	nb;
	int	neg;
	int	i;

	nb = 0;
	neg = 1;
	i = 0;
	if (str[i] == '-')
	{
		neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb *= 10;
		nb += str[i] - '0';
		i++;
	}
	return (nb * neg);
}
